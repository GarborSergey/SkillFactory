# получает список из чисел, возвращает словарь, где значение каждого ключа
# сколько раз оно встретилось в списке
# ________________________________________________________________________________
# мое решение
numbers = [1, 2, 1, 3, 3, 3]


def numbers_repeat(numbs: list):
    # С помощью генератора словарей создаю словарь
    # в котором значение ключа есть цифра из списка, а значение кол-во повторений в этом списке
    # с помощью метода count
    return {item: numbs.count(item) for item in numbs}


print(numbers_repeat(numbers))
