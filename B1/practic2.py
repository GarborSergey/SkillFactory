# проверка соответствует ли введенная строка формату YYYY-MM-DD
# ________________________________________________________________________________
# мое решение
d = '2022-02-21'
d1 = '2022-42-21'


def check(date):
    date_list = date.split('-')
    if len(date_list) == 3 and len(date_list[0]) == 4 and 1 <= int(date_list[1]) <= 12 and 1 <= int(date_list[2]) <= 31:
        return True
    else:
        return False

print(check(d))
print(check(d1))
